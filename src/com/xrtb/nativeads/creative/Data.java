package com.xrtb.nativeads.creative;

/**
 * A class that represents the fields found in a bid request for a Data asset of a native ad.
 * @author Ben M. Faul
 *
 */
public class Data {
	/** The required flag as defined in the RTB Native Ad spec */
	public int required;
	/** The len flag as defined in the RTB Native Ad spec */
	public int len;
	/** The type flag as defined in the RTB Native Ad spec */
	public int type;
	
}
